    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        String username = txtUsername.getText().trim();
        String password = txtPassword.getText().trim();
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập tài khoản!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập mật khẩu!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        currentAccount = dao.login(username, password);
        if(currentAccount == null) {
            JOptionPane.showMessageDialog(null, "Sai tài khoản hoặc mật khẩu!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        new MainForm(this).setVisible(true);
        txtUsername.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_loginBtnActionPerformed

